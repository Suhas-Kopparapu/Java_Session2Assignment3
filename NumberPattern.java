package Assignments;

public class NumberPattern {
	
	public static void main(String[] args){
		
		for(int m=1;m<=4;m++){
			System.out.println("");
			for(int n=1;n<=m;n++){
				System.out.print(n);
			}
			
		}
		

		for(int i=1,r=5;i<=5;i++,r--)
		{
			System.out.println("");
			for(int j=1;j<=r;j++)
			{
				System.out.print(j);
			}
			
		}
		
		
	}

}
 